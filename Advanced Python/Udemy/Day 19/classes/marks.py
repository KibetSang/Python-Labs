from data import list
class Mark:
    def __init__(self, mark):
        self.mark = mark

    def grade(grade):
        if grade >= 90:
            return 'A'
        elif grade >= 80:
            return 'B'
        elif grade >= 70:
            return 'C'
        elif grade >= 60:
            return 'D'
        else:
            return 'F'
    def print_marks(self):
        print(self.mark)